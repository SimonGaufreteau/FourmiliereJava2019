package Interfaces_Global;

/*
Interface Deposer. Implémente la méthode "deposer():boolean" qui permet à l'objet de deposer
l'objet porté sur la case sur laquelle il se trouve.
Renvoie "true" si l'objet possède quelque chose à deposer (ex : Nourriture), "false" sinon.
 */

public interface Deposer {
    boolean deposer();
}
